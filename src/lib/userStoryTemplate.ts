export function getUserStoryPrompt(baseInfo: string, transcript: string): string {
  return `당신은 애자일 코치입니다. 다음 회의 내용을 바탕으로 **사용자 스토리 문서**를 작성해주세요.

${baseInfo}

## 원본 회의 내용 (분석 대상)
${transcript}

---

## 작성 가이드

**중요**: 위 회의 내용에서 실제 논의된 사용자 요구사항을 **추출**하여 작성하세요.
회의 내용에 없는 내용을 지어내지 말고, 논의된 사용자 스토리를 바탕으로 구조화하세요.

## 사용자 스토리 문서 구조

### 1. 사용자 페르소나

#### 1.1 데이터 분석가
- **직군**: 데이터 분석가
- **목표**: 실시간 데이터 모니터링
- **기술 수준**: 높음
- **페인 포인트**: 데이터 로딩 지연

#### 1.2 경영진
- **직군**: 경영진
- **목표**: KPI 확인
- **기술 수준**: 낮음
- **페인 포인트**: 복잡한 리포트

### 2. 에픽

#### Epic 1: 실시간 대시보드
사용자가 실시간으로 변하는 데이터를 모니터링할 수 있다.

### 3. 사용자 스토리

#### US-001: 대시보드 조회
**As a** 데이터 분석가
**I want** 실시간으로 업데이트되는 대시보드를 보고 싶다
**So that** 즉시 데이터 변화를 감지하고 대응할 수 있다

**Acceptance Criteria:**
- **Given** 사용자가 로그인되어 있고
- **When** 대시보드 화면에 접속하면
- **Then** 1초 이내에 최신 데이터가 표시되어야 한다

#### US-002: 위젯 커스터마이징
**As a** 데이터 분석가
**I want** 위젯을 드래그하여 배치하고 싶다
**So that** 내가 원하는 대로 화면을 구성할 수 있다

**Acceptance Criteria:**
- **Given** 사용자가 편집 모드이고
- **When** 위젯을 드래그하면
- **Then** 위젯이 새 위치에 배치되고 저장되어야 한다

### 4. 사용자 시나리오 (User Journey)

주요 사용자 경로 단계별 매핑:
1. [단계1] → 2. [단계2] → 3. [단계3]

회의 내용을 바탕으로 구체적인 사용자 스토리를 작성하세요.`;
}
