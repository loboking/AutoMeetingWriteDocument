// 플로우차트 템플릿
export function getFlowchartPrompt(baseInfo: string, transcript: string): string {
  return `당신은 프로세스 디자이너입니다. 다음 회의 내용을 바탕으로 **플로우차트 문서**를 작성해주세요.

${baseInfo}

## 원본 회의 내용 (분석 대상)
${transcript}

---

## 작성 가이드

**중요**: 위 회의 내용에서 실제 논의된 프로세스 흐름을 **추출**하여 작성하세요.
회의 내용에 없는 내용을 지어내지 말고, 논의된 흐름을 바탕으로 시각화하세요.

## 플로우차트 문서 구조

### 1. 프로세스 개요
- **프로세스 명**: [회의에서 논의된 주요 프로세스]
- **목적**: [프로세스의 목적]
- **관련 시스템**: [연동 시스템]

### 2. 주요 시나리오 플로우

#### 2.1 회원가입 플로우

**단계**:
1. 사용자가 회원가입 버튼 클릭
2. 이메일 입력
3. 비밀번호 입력
4. 약관 동의
5. 가입 요청 전송
6. 이메일 인증 전송
7. 이메일 인증 확인
8. 회원가입 완료

**Mermaid 다이어그램**:
\`\`\`mermaid
flowchart TD
    A[시작] --> B[회원가입 버튼 클릭]
    B --> C[이메일 입력]
    C --> D{이메일 유효성 검사}
    D -->|실패| C
    D -->|성공| E[비밀번호 입력]
    E --> F{비밀번호 복잡도 검사}
    F -->|실패| E
    F -->|성공| G[약관 동의]
    G --> H[가입 요청]
    H --> I{이메일 중복 검사}
    I -->|중복| J[에러 메시지]
    I -->|가능| K[인증 이메일 전송]
    K --> L[이메일 인증 확인]
    L --> M[회원가입 완료]
    M --> N[종료]
    J --> C
\`\`\`

#### 2.2 로그인 플로우

**단계**:
1. 사용자가 로그인 화면 진입
2. 이메일/비밀번호 입력
3. 로그인 요청
4. 인증 확인
5. 토큰 발급
6. 대시보드 이동

**Mermaid 다이어그램**:
\`\`\`mermaid
flowchart TD
    A[시작] --> B[이메일/비밀번호 입력]
    B --> C[로그인 요청]
    C --> D{인증 확인}
    D -->|실패| E[에러 메시지]
    D -->|성공| F[JWT 토큰 발급]
    F --> G[대시보드 이동]
    G --> H[종료]
    E --> B
\`\`\`

### 3. 예외 처리 플로우

#### 3.1 인증 실패 시나리오
| 예외 상황 | 처리 방법 |
|----------|----------|
| 이메일 중복 | 중복 안내 + 다른 이메일 입력 요청 |
| 비밀번호 불일치 | 불일치 안내 + 재입력 요청 |
| 인증 이메일 미수신 | 재전송 버튼 제공 |

#### 3.2 네트워크 에러 처리
\`\`\`mermaid
flowchart TD
    A[API 요청] --> B{응답 수신}
    B -->|성공| C[정상 처리]
    B -->|네트워크 에러| D[재시도 팝업]
    D --> E{사용자 선택}
    E -->|재시도| A
    E -->|취소| F[에러 페이지]
\`\`\`

### 4. 시스템 간 연동 플로우

#### 4.1 인증 연동
\`\`\`mermaid
sequenceDiagram
    participant U as 사용자
    participant F as 프론트엔드
    participant B as 백엔드
    participant A as 인증 서버

    U->>F: 로그인 요청
    F->>B: POST /api/auth/login
    B->>A: 토큰 검증 요청
    A-->>B: 토큰 발급
    B-->>F: 로그인 성공 + 토큰
    F-->>U: 대시보드 이동
\`\`\`

### 5. 비즈니스 로직 플로우

#### 5.1 데이터 처리 플로우
\`\`\`mermaid
flowchart LR
    A[데이터 수집] --> B[검증]
    B --> C[변환]
    C --> D[저장]
    D --> E[알림 발송]
\`\`\`

회의 내용을 바탕으로 실제 프로세스 플로우를 시각화하세요.`;
}
