// 테스트 계획 템플릿
export function getTestPlanPrompt(baseInfo: string, transcript: string): string {
  return `당신은 QA 리드입니다. 다음 회의 내용을 바탕으로 **테스트 계획서**를 작성해주세요.

${baseInfo}

## 원본 회의 내용 (분석 대상)
${transcript}

---

## 작성 가이드

**중요**: 위 회의 내용에서 실제 논의된 테스트 요구사항을 **추출**하여 작성하세요.
회의 내용에 없는 내용을 지어내지 말고, 논의된 테스트 범위를 바탕으로 계획을 수립하세요.

## 테스트 계획서 구조

### 1. 테스트 개요
- **테스트 목표**: 회의에서 논의된 기능의 품질 보증
- **테스트 범위**: [기능 목록]
- **테스트 기간**: X주
- **테스트 팀**: [팀 구성]

### 2. 테스트 전략

#### 2.1 테스트 접근 방식
- **방법론**: 애자일 테스트
- **테스트 주기**: 스프린트별 테스트
- **테스트 환경**: Staging 환경

#### 2.2 테스트 유형별 전략

| 테스트 유형 | 목적 | 도구 | 책임자 | 주기 |
|------------|------|------|--------|------|
| 단위 테스트 | 함수/컴포넌트 품질 | Jest/Vitest | 개발자 | 매일 |
| 통합 테스트 | 모듈 간 연동 | Supertest | 개발자 | 주별 |
| API 테스트 | API 동작 검증 | Postman | QA | 주별 |
| E2E 테스트 | 사용자 시나리오 | Playwright | QA | 스프린트별 |
| 성능 테스트 | 부하/응답시간 | k6 | QA | 릴리스 전 |
| 보안 테스트 | 취약점 점검 | OWASP ZAP | 보안팀 | 분별 |

### 3. 테스트 범위

#### 3.1 In-Scope (테스트 대상)
- [ ] 회원가입/로그인
- [ ] 대시보드
- [ ] 데이터 조회/필터
- [ ] 설정/프로필
- [ ] API 엔드포인트
- [ ] 데이터베이스 연동

#### 3.2 Out-of-Scope (테스트 제외)
- [ ] 서드파티 라이브러리 내부
- [ ] 프로덕션 데이터 백업

### 4. 테스트 환경

#### 4.1 환경 구성
| 환경 | 용도 | URL | 데이터 |
|------|------|-----|--------|
| Local | 개발 테스트 | localhost | Mock 데이터 |
| Dev | 개발 통합 | dev.example.com | 테스트 데이터 |
| Staging | 사전 테스트 | staging.example.com | 실제 데이터와 유사 |
| Production | 운영 (모니터링) | app.example.com | 실제 데이터 |

#### 4.2 테스트 데이터
- **정상 데이터**: 표준 사용자 시나리오
- **경계 데이터**: 최소/최대값
- **예외 데이터**: 오류 유발 케이스

### 5. 테스트 일정

| 단계 | 작업 | 기간 | 담당자 |
|------|------|------|--------|
| 1단계 | 테스트 계획 수립 | 2일 | QA 리드 |
| 2단계 | 테스트 케이스 작성 | 3일 | QA 팀 |
| 3단계 | 테스트 환경 구축 | 2일 | DevOps |
| 4단계 | 테스트 실행 | 1주 | QA 팀 |
| 5단계 | 버그 리포트 및 수정 | 3일 | 개발팀 |
| 6단계 | 회귀 테스트 | 2일 | QA 팀 |
| 7단계 | 최종 승인 | 1일 | PO |

### 6. 입수/퇴수 기준

#### 6.1 입수 기준 (Entry Criteria)
- [ ] 개발 완료 및 Dev 배포
- [ ] 단위 테스트 통과 (80% 이상)
- [ ] 코드 리뷰 완료
- [ ] 테스트 환경 준비 완료

#### 6.2 퇴수 기준 (Exit Criteria)
- [ ] P0/P1 버그 0건
- [ ] P2 버그 3건 이하
- [ ] 테스트 커버리지 70% 이상
- [ ] 성능 기준 충족
- [ ] 보안 점검 통과

### 7. 리스크 관리

| 리스크 | 확률 | 영향 | 대응 방안 |
|--------|------|------|----------|
| 테스트 인력 부족 | 중 | 높 | 외부 QA 지원 |
| 환경 불안정 | 중 | 중 | 환경 사전 점검 |
| 일정 지연 | 높 | 높 | 우선순위 조정 |

### 8. 테스트 결과 보고

#### 8.1 보고 주기
- **일일**: 데일리 스큠럼에서 공유
- **주별**: 주간 테스트 리포트
- **종료**: 최종 테스트 결과서

#### 8.2 보고 형식
| 항목 | 내용 |
|------|------|
| 테스트 케이스 수 | N건 |
| 실행 수 | N건 |
| 통과 | N건 |
| 실패 | N건 |
| 버그 현황 | P0: N, P1: N, P2: N |
| 테스트 커버리지 | N% |

회의 내용을 바탕으로 실제 테스트 계획을 수립하세요.`;
}
